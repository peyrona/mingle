    private void btnAddRowActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAddRowActionPerformed
    {//GEN-HEADEREND:event_btnAddRowActionPerformed
        stopEditing();

        DefaultTableModel model = (DefaultTableModel) tblConfig.getModel();
        model.addRow( new Object[]{ null, null } );

        int lastRow = model.getRowCount() - 1;
        tblConfig.setRowSelectionInterval( lastRow, lastRow );
        tblConfig.scrollRectToVisible( tblConfig.getCellRect( lastRow, 0, true ) );
        tblConfig.editCellAt( lastRow, 0 );

        if( tblConfig.getEditorComponent() != null )
        {
            tblConfig.getEditorComponent().requestFocusInWindow();
        }
    }//GEN-LAST:event_btnAddRowActionPerformed

    private void btnDelRowActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDelRowActionPerformed
    {//GEN-HEADEREND:event_btnDelRowActionPerformed
        int selectedRow = tblConfig.getSelectedRow();

        if( selectedRow < 0 )
            return;

        stopEditing();

        DefaultTableModel model = (DefaultTableModel) tblConfig.getModel();
        model.removeRow( selectedRow );

        // Select the nearest remaining row
        int rowCount = model.getRowCount();

        if( rowCount > 0 )
        {
            int newSelection = Math.min( selectedRow, rowCount - 1 );
            tblConfig.setRowSelectionInterval( newSelection, newSelection );
        }
    }//GEN-LAST:event_btnDelRowActionPerformed


