    /**
     * Returns the configuration as a map of key-value pairs.
     * <p>
     * Rows with blank or null names are skipped. Keys are converted to
     * lowercase to match the transpiler convention.
     *
     * @return a map with the configuration entries (never {@code null}).
     */
    public Map<String,Object> getConfig()
    {
        stopEditing();

        DefaultTableModel  model  = (DefaultTableModel) tblConfig.getModel();
        Map<String,Object> config = new HashMap<>();

        for( int row = 0; row < model.getRowCount(); row++ )
        {
            Object oName  = model.getValueAt( row, 0 );
            Object oValue = model.getValueAt( row, 1 );

            if( oName == null )
                continue;

            String name = oName.toString().trim();

            if( name.isEmpty() )
                continue;

            config.put( name.toLowerCase(), (oValue != null ? oValue : "") );
        }

        return config;
    }

    /**
     * Populates the table with the entries from the given configuration map.
     * <p>
     * Any previous content in the table is removed before loading.
     *
     * @param config the configuration to load ({@code null} or empty clears the table).
     */
    public void setConfig( Map<String,Object> config )
    {
        stopEditing();

        DefaultTableModel model = (DefaultTableModel) tblConfig.getModel();
                          model.setRowCount( 0 );

        if( config != null )
        {
            for( Map.Entry<String,Object> entry : config.entrySet() )
            {
                model.addRow( new Object[]{ entry.getKey(), entry.getValue() } );
            }
        }
    }

