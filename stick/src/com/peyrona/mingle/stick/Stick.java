    // INNER CLASS
    // A Bus Listener to read message (mainly form Controllers).
    //---------------------------------------------------------------------------//

    /**
     * This is just an interface needed by the BusListener class.
     */
    @FunctionalInterface
    private interface MessageHandler
    {
        void handle( Message message );
    }

    /**
     * BusListener: this class is the messages orchestrator for the bus.
     *
     * When arrived to destinations, messages are processed by the inner class
     * ::ClientListener. In other words, from the point of the view of clients
     * connected to an ExEn, ::BusListener is the one that sends messages to
     * the clients and ::ClientListener the one that receives messages from them.
     *
     * An ExEn sends only messages produced by itself (using a NetServer),
     * therefore an ExEn can not resend messages received from another ExEn or
     * tool (like Glue or Gum), but messages to be sent are sent to every
     * connected entity (using INetClient).
     */
    private final class BusListener implements IEventBus.Listener<com.peyrona.mingle.lang.messages.Message>
    {
        private final Map<Class<?>, MessageHandler> msgHandler;       // Pre-computed dispatch map for O(1) message routing
        private final Map<String  , IDriver>        driver4device;    // Driver associated with a device (by its name)

        @Override
        public void onMessage( Message message )
        {
            msgHandler.get( message.getClass() )
                      .handle( message );    // If there is no handler for the messsage, a null point exc is triggered: this is intended
        }

        //------------------------------------------------------------------------//
        // PRIVATE INTERFACE

        private BusListener()
        {
            driver4device = new HashMap<>( 5 );

            msgHandler = Map.of( MsgDeviceReaded.class  , this::handleDeviceReaded,
                                 MsgDeviceChanged.class , this::handleDeviceChanged,
                                 MsgChangeActuator.class, this::handleChangeActuator,
                                 MsgExecute.class       , this::handleExecute,
                                 MsgReadDevice.class    , this::handleReadDevice );
        }

        private void handleDeviceReaded( Message message )
        {
            final MsgDeviceReaded msg = (MsgDeviceReaded) message;

            // If message::isOwn is true  the device exists because the msg was generated in this ExEn.
            // If message::isOwn is false the device exists because it was checked by ::ServerListener.

            if( msg.isOwn )
            {
                if( deviMgr.named( msg.name ).value( msg.value ) )    // If device effectively changed its value
                {
                    ruleMgr.forEach( rule -> rule.eval( msg.name, msg.value ) );
                    broadcast( message );
                }
            }
            else
            {
                ruleMgr.forEach( rule -> rule.eval( msg.name, msg.value ) );
            }
        }

        private void handleDeviceChanged( Message message )
        {
            final MsgDeviceChanged msg = (MsgDeviceChanged) message;

            // If message::isOwn is true  the device exists because the msg was generated in this ExEn.
            // If message::isOwn is false the device exists because it was checked by ::ServerListener.

            if( msg.isOwn )
            {
                if( deviMgr.named( msg.name ).value( msg.value ) )    // If device effectively changed its value
                {
                    ruleMgr.forEach( rule -> rule.eval( msg.name, msg.value ) );
                    broadcast( message );
                }
            }
            else
            {
                ruleMgr.forEach( rule -> rule.eval( msg.name, msg.value ) );
            }
        }

        private void handleChangeActuator( Message message )
        {
            final MsgChangeActuator msg    = (MsgChangeActuator) message;
            final IDriver           driver = getDriver4Device( msg.name );

            // If message::isOwn is true  the device exists because the msg was generated in this ExEn.
            // If message::isOwn is false the device exists because it was checked by ::ServerListener.

            if( driver != null )     // There could be a problem loading the driver
                driver.write( msg.name, msg.value );
            else
                log( ILogger.Level.SEVERE, "Driver for device '"+ msg.name +"' was not found" );

            broadcast( message );    // Perhaps an actuator with same name is in another grid node
        }

        private void handleExecute( Message message )
        {
            final MsgExecute msg  = (MsgExecute) message;
            final IRule      rule = ruleMgr.named( msg.name );

            // If message::isOwn is true  the rule/script exists because the msg was generated in this ExEn.
            // If message::isOwn is false the rule/script exists because it was checked by ::ServerListener.

            if( rule != null )
                rule.trigger( (boolean) msg.value );
            else    // Must be an script
                srptMgr.named( msg.name ).execute();

            broadcast( message );    // Perhaps a rule or script with same name is in another grid node
        }

        private void handleReadDevice( Message message )
        {
            final MsgReadDevice msg    = (MsgReadDevice) message;
            final IDriver       driver = getDriver4Device( msg.name );

            // If message::isOwn is true  the device exists because the msg was generated in this ExEn.
            // If message::isOwn is false the device exists because it was checked by ::ServerListener.

            if( driver != null )     // There could be a problem loading the driver
                driver.read( msg.name );
            else
                log( ILogger.Level.SEVERE, "Driver for device '"+ msg.name +"' was not found" );

            broadcast( message );    // Perhaps a rule or script with same name is in another grid node
        }

        private void broadcast( Message message )
        {
            if( message.isOwn && isGridNode() )
                gridMgr.broadcast( message );   // Internally uses a thread
        }

        private IDriver getDriver4Device( String name )
        {
            IDriver driver = driver4device.get( name );

            if( driver == null )
            {
                driver = drvrMgr.first( drv -> drv.has( name ) );

                if( driver != null )
                {
                    driver4device.put( name, driver );
                }
                else if( deviMgr.named( name ) != null )    // Only an error if the device still exists (1)
                {
                    log( ILogger.Level.SEVERE, "No driver for device '"+ name +"'\nCheck Stick log file." );
                }
            }

            return driver;

            // (1) When eventBus.resume() is called after each removal step, in-flight MsgReadDevice
            //     messages for already-removed devices are flushed. The handler logs SEVERE because
            //     the driver is gone — but the device is simply gone too.
        }
    }
