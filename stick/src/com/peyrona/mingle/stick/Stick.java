/**
 * This class provides the execution environment where the transpiled Une code is executed.<br>
 * <br>>
 * Although it is not frequent, several ExEn instances could coexists even in the same
 * JVM: it is responsibility of the application that creates the instances to properly
 * manage them.<br>
 * <br>>
 * Because messages inside an ExEn are main generated by devices and because there is a very
 * limited amount of devices, I was thinking to reuse the same messages and simply change its
 * value.<br>
 * After thinking, this is not a good idea: the performance gain from reusing message
 * instances is negligible compared to the risk of race conditions and data corruption.
 *
 * @author Francisco José Morero Peyrona
 *
 * Official web site at: <a href="https://github.com/peyrona/mingle">https://github.com/peyrona/mingle</a>
 */
