    /**
     * Informs to a device about the down-timed devices.
     *
     * @param sReporterDevName The name of the device to be informed: the one that receives the list of down-timed devices.
     */
    private void checkDowntimedDevices( final String sReporterDevName )
    {
        IDevice dev2Report = deviMgr.named( sReporterDevName );    // The device used to store the list of downtimed devices

        if( dev2Report != null )    // Because devices can be deleted at runtime
        {
            list lst = new list();

            deviMgr.forEach( dev ->
                             {
                                if( dev.isDowntimed() )
                                    lst.add( dev.name() );
                             } );

            if( ! lst.isEmpty() )
                dev2Report.value( lst );
        }
    }

    private void failed( Exception exc, String msg )
    {
        if( msg == null )
            msg = "";

        msg += "\nCan not continue. Check logs.\n\n"+ UtilStr.toString( exc );

        exit( 0, 1, msg );    // ::stop() is always invoked by a System hook
    }

    // There is a hook that invokes this method on JMV exiting
    private Stick stop()
    {
        if( bExited )
            return this;

        // In case the runVoidThread() method was invoked -------------
        if( ! keepRun )
            return this;   // Already stopped

        say( "Shutting down Stick ..." );

        keepRun = false;   // Signal thread to stop

        synchronized( this.config )
        {
            this.config.notifyAll();   // Wake up the void thread so it can exit the loop
        }
        //-------------------------------------------------------------

        // Execute PRE ONSTOP scripts BEFORE stopping EventBus
        // (scripts can still communicate with devices via the bus)
        srptMgr.runPreStopScripts();

        eventBus.flush();  // Wait for PRE ONSTOP messages to be delivered
        eventBus.stop();
        ruleMgr.stop();
        drvrMgr.stop();
        deviMgr.stop();

        if( isGridNode() )
            gridMgr.stop();

        srptMgr.stop();    // Last one to trigger SCRIPTs ONSTOP after all is stopped (entities are gone)

        UtilSys.stop();

        try
        {
            Thread.sleep( 2500 );   // A long delay time (there is no rush, guys) to allow any pending tasks (if any) to finish
        }
        catch( InterruptedException e )
        {
            // Nothing to do
        }

        say( "<<<<<<< Stick finished >>>>>>>" );

        return this;
    }

    /**
     * Sorts passed list in the needed order to be added (or removed): Scripts, Drivers, Devices and Rules.
     *
     * @param commands List to be sorted.
     * @param bReverse true when the resulting list is needed to remove commands instead of adding them.
     * @return Passed list after being sorted
     */
    private List<ICommand> sortByType( List<ICommand> list, boolean bReverse )
    {
        if( list.size() > 1 )
        {
            list.sort( (cmd1, cmd2) -> getPrecedence( cmd1 ).compareTo( getPrecedence( cmd2 ) ) );

            if( bReverse )
                Collections.reverse( list );
        }

        return list;
    }

    private Integer getPrecedence( ICommand cmd )
    {
             if( cmd instanceof ILibrary )  return 0;    // Highest precedence: functions must be available before scripts
        else if( cmd instanceof IScript  )  return 1;
        else if( cmd instanceof IDriver  )  return 2;
        else if( cmd instanceof IDevice  )  return 3;
                                            return 4;    // Lowest precedence (IRule)
    }

    /**
     * JVM needed at least one daemon thread to not exit: this approach (to create a void
     * thread) is as good as any other approach.
     * <p>
     * The thread lives in the JVM and there is no need to have a reference to this thread:
     * the only case this reference would be needed is to stop this thread, but this is done
     * only when Stick is going to be stopped, and it is simpler to invoke System.exit(0):
     * this stops all threads, invokes the shutdown-hooks and exists.
     */
    private void runVoidThread()
    {
        Thread voidTh = new Thread( () ->
            {
                while( keepRun )
                {
                    synchronized( Stick.this.config )     // Config -> just a final var
                    {
                        try
                        {
                            Stick.this.config.wait();
                        }
                        catch( InterruptedException e )   // Handle interruption to exit loop
                        {
                            break;
                        }
                    }
                }
            } );

        voidTh.setName( "Stick-KeepAlive" );
        voidTh.start();
    }

    private static int classMask( String s )    // s --> Class Name (Device, Script, Rule, Library)
    {
        if( s == null )
            return 0;

        // 1. Manual Trim (Zero Allocation)
        int start = 0, end = s.length();

        while( start < end && Character.isWhitespace( s.charAt( start ) ) )
            start++;

        while( end > start && Character.isWhitespace( s.charAt( end - 1 ) ) )
            end--;

        // 2. Manual Plural Check
        if( end > start )
        {
            char last = s.charAt( end - 1 );

            if( last == 's' || last == 'S' )
                end--;
        }

        int len = end - start;

        if( len == 0 )
            return 0;

        // 3. Switch on first char to reduce comparison cost
        //    We bitwise-OR with 32 to force lowercase (ASCII trick) for the char check
        char first = s.charAt( start );

        switch( first | 32 )    // 'D' | 32 = 'd', 'R' | 32 = 'r'
        {
            case 'd':
                if( len == 6 )
                {
                    // Only check "device" or "driver" if length matches 6
                    // Differentiating by the second char 'e' vs 'r' is even faster
                    char second = s.charAt( start + 1 );

                    if( (second | 32) == 'e' && s.regionMatches( true, start, "device", 0, 6 ) )
                        return 1;

                    if( (second | 32) == 'r' && s.regionMatches( true, start, "driver", 0, 6 ) )
                        return 2;
                }
                break;

            case 's':
                if( len == 6 && s.regionMatches( true, start, "script", 0, 6 ) )
                    return 4;

                break;

            case 'r':
                if( len == 4 && s.regionMatches( true, start, "rule", 0, 4 ) )
                    return 8;

                break;

            case 'l':
                if( len == 7 && s.regionMatches( true, start, "library", 0, 7 ) )
                    return 16;

                break;
        }

        return 0;
    }

