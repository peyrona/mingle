    Object getValue()
    {
        Object value = null;

        try
        {
                 if( radBoolean.isSelected() ) value = UtilType.toBoolean( txtValue.getText() );
            else if( radNumber.isSelected()  ) value = UtilType.toFloat(   txtValue.getText() );
            else                               value = txtValue.getText();   // No trim

            if( value == null )
                throw new NullPointerException();
        }
        catch( MingleException | NullPointerException | NumberFormatException exc )
        {
            JTools.alert( "Error converting value" );
        }

        return value;
    }

